//The Role of Purposes
//Authors&#8217; and audiences&#8217; purposes in communicating determine the basic rationale behind other decisions both authors and audiences make (such as what to write or speak about, or whom to listen to, or what medium to use, or what setting to read in, among others). An author&#8217;s purpose in communicating could be to instruct, persuade, inform, entertain, educate, startle, excite, sadden, enlighten, punish, console, or many, many others. Like authors, audiences have varied purposes for reading, listening to, or otherwise appreciating pieces of communication. Audiences may seek to be instructed, persuaded, informed, entertained, educated, startled, excited, saddened, enlightened, punished, consoled, or many, many others. Authors&#8217; and audiences&#8217; purposes are only limited to what authors and audiences want to accomplish in their moments of communication. There are as many purposes for communicating as there are words to describe those purposes.
//Attitude
//Attitude is related to purpose and is a much-overlooked element of rhetorical situations. But attitude affects a great deal of how a rhetorical situation unfolds. Consider if an author communicates with a flippant attitude as opposed to a serious attitude, or with drama as opposed to comedy, or calmly as opposed to excitedly. Depending on authors&#8217; purposes, audiences&#8217; specific qualities, the nature of the context, and other factors, any of these attitudes could either help or hinder authors in their efforts to communicate depending on the other factors in any given rhetorical situation. Like authors, audiences bring diverse attitudes to how they appreciate different pieces of communication. The audience&#8217;s attitude while reading, listening, observing, or whatnot affects how they receive and process the communication they receive.
